class LogSumExp(TensorOp):
    def __init__(self, axes: Optional[tuple] = None):
        self.axes = axes

    def compute(self, Z):
        ### BEGIN YOUR SOLUTION      
        max_z_original = array_api.max(Z, self.axes, keepdims=True) 
        max_z = array_api.max(Z, self.axes)
        return array_api.log(array_api.sum(array_api.exp(Z - max_z_original), self.axes)) + max_z
        ### END YOUR SOLUTION

    def gradient(self, out_grad, node):
        ### BEGIN YOUR SOLUTION
      z = node.inputs[0]
      max_z = z.realize_cached_data().max(self.axes, keepdims=True)
      exp_z = exp(z - max_z)
      sum_exp_z = summation(exp_z, self.axes)
      grad_sum_exp_z = out_grad / sum_exp_z
      if self.axes is None:
        expand_shape = [1 for axis, dim in enumerate(z.shape)]

      else:
        expand_shape = [1 if axis in self.axes else dim for axis, dim in enumerate(z.shape)]

      grad_exp_z = grad_sum_exp_z.reshape(expand_shape).broadcast_to(z.shape)
      return grad_exp_z * exp_z
        ### END YOUR SOLUTION


def logsumexp(a, axes=None):
    return LogSumExp(axes=axes)(a)
